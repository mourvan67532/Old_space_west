def opcao_1(c):
    print("Opção 1:",a[c]['op1'])

def opcao_2(c):
    print("Opção 2:",a[c]['op2'])


def escolhido_1(d):
    chance_1 = random.randint(0, 100)
    csv_chance_1 = a[d]['chance_s_1']
    if chance_1 >= csv_chance_1:
        print(a[d]['sucesso_1'])
    else:
        print(a[d]['derrota_1'])

def escolhido_2(d):
    chance_2 = random.randint(0, 100)
    csv_chance_2 = a[d]['chance_s_2']
    if chance_2 >= csv_chance_2:
        print(a[d]['sucesso_2'])
    else:
        print(a[d]['derrota_2'])


def opcao_escolher(c):
    escolheu = int(input("Qual opção você escolhe: "))
    if escolheu == 1:
        escolhido_1(c)
    elif escolheu == 2:
        escolhido_2(c)
    


def play():
    numero = random.randint(0, 40)
    enviar_ave(numero)
    opcao_1(numero)
    opcao_2(numero)
    opcao_escolher(numero)






